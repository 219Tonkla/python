def addition(x, y):
    return x + y

def subtraction(x, y):
    return x - y

def multiplication(x, y):
    return x * y

# ทดสอบการทำงานของฟังก์ชัน
result_add = addition(5, 3)
result_sub = subtraction(10, 4)
result_mul = multiplication(6, 2)

print("ผลลัพธ์การบวก:", result_add)
print("ผลลัพธ์การลบ:", result_sub)
print("ผลลัพธ์การคูณ:", result_mul)